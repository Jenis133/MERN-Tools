//if gender is male => He is male
//if gender is female => she is female
//if gender is other than male and female  => other
// else none

let gender = "male";

if (gender === "male") {
  console.log("He is male");
} else if (gender === "female") {
  console.log("She is female");
} else if (gender === "other") {
  console.log("other");
} else {
  console.log("none");
}
