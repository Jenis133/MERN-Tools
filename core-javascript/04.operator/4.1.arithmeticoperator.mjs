// % is remainder operator. Do not mistake it for division which is "/".
console.log(4 % 2); //the remainder is 0
console.log(4 % 3); //the remainder is 1
console.log(4 % 5); //the remainder is 4 since the divider value is high
console.log(4 % 40); //the remainder is 4 since the divider value is high