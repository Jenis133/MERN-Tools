//Else-if for a single chain loop

let gender = "Male";

if (gender === "Male") {
    console.log("He is a male.");
}
else if (gender === "Female") {
    console.log("She is a female");
}
else if (gender === "Other") {
    console.log("They are other.");
}
else {
    console.log("None");
}
console.log();

