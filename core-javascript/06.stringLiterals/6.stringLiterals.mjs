let name = "Sid";
let surName = "Sherpa";
let fullName = `My name is ${name} and my surname is ${surName}.`;
console.log(fullName);
console.log();

// Using single quote
fullName = 'My name is ${name} and my surname is ${surName}.';
console.log(fullName);
console.log();

// Using double quote
fullName = "My name is ${name} and my surname is ${surName}.";
console.log(fullName);
console.log();